package sept.ex_25092024;

public class Lab040 {

	public static void main(String[] args) {
	//Loops can execute a block of code as long as a specified condition is reached.Loops are handy because they save time, reduce errors, and they make code more readable.
// repeating a task again n again 
		
		for (int i=1; i<=10; i++) {
		System.out.println(i);
		}
		for (int ashwarya = 1 ; ashwarya < 8 ; ashwarya++) {
			
			System.out.println(ashwarya);
		}
		for(int _1 = 0 ; _1 <10 ; _1 ++) { 
			System.out.println(_1);
		}
		
		  // for( A ;B ; C)
        // A -> Initialization -> JVM What variable, where you are starting point
        // B - Condition -> When you want this to stop.
        // C ->  Increment / Decrement
		
		
		for (int a = 0; a>1 ; a++) {
			System.out.println(a);
		}
	
	}

}
